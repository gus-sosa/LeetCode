class Solution
{
private:
    int add(int n)
    {
        int sum = 0;
        while (n > 0)
        {
            sum += n % 10;
            n /= 10;
        }
        return sum;
    }

public:
    int addDigits(int num)
    {
        return num < 10 ? num : addDigits(add(num));
    }
};
